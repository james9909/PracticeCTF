def bin2ascii(string):
    hexx = []
    i = 0
    while i < len(string):
        hexx.append(hex(int(string[i:i+8], 2)))
        i = i +8
    s = ''
    for block in hexx:
        s += str(chr(int(block, 16)))
    return s

